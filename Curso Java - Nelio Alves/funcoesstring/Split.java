package funcoesstring;

public class Split {
    public static void main(String[] args) {
        String s = "Kevin Richard Lindo";

        String[] vector = s.split(" ");
        System.out.println((vector[0]));
        System.out.println((vector[1]));
        System.out.println((vector[2]));
    }
}

