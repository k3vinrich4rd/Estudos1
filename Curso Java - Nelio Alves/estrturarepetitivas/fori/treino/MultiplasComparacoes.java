package estrturarepetitivas.fori.treino;

public class MultiplasComparacoes {
    public static void main(String[] args) {
        for (int i = 0, j = 0; i < 10 && j < 10; i++, j++) {
            System.out.println("Value i: " + i);
            System.out.println("Value j: " + j);
        }
    }
}
