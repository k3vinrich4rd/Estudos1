package estrtuturaderepeticao;
//For - normal (mais usado):
public class ForExemplo1 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        for (int i = 5; i < 100; i++) {
            System.out.println(i);
        }

        for (int i = 5; i < 140; i++) {
            System.out.println(i);
        }

        for (int i = 0; i < 245; i++) {
            System.out.println(i);
        }

        for (int i = 0; i < 100; i++){
            System.out.println(i);
        }

        for (int i = 100; i > 1; i --){
            System.out.println(i);
        }



    }
}
