package arraymultidimensionais;

public class Exemplo2 {
    public static void main(String[] args) {
        int[][] notasDosAlunos = {{1, 2, 3, 4, 5, 6}, {10, 5, 7, 8, 9, 6}};
        System.out.println(notasDosAlunos[1][0]);
        System.out.println(notasDosAlunos[1][1]);
        System.out.println(notasDosAlunos[1][2]);
        System.out.println(notasDosAlunos[1][3]);
        System.out.println(notasDosAlunos[1][4]);
        System.out.println(notasDosAlunos[1][5]);

        System.out.println(notasDosAlunos[0][3]);
        System.out.println(notasDosAlunos[1][1]);
        System.out.println(notasDosAlunos[1][2]);
        System.out.println(notasDosAlunos[1][3]);
        System.out.println(notasDosAlunos[1][4]);
        System.out.println(notasDosAlunos[1][5]);
    }
}
